
public class Main {

	public static void main(String[] args) {
		
		String x = "Developer";
		String y = "DarkSide";
		String temp;
		
		System.out.println("x: "+x);
		System.out.println("y: "+y);
		
		System.out.println("\nAfter Swapping\n");
		
		temp = x;
		x = y;
		y = temp;
		
		System.out.println("x: "+x);
		System.out.println("y: "+y);

	}

}
